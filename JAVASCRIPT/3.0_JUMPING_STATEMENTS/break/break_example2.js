let fruit = 'apple';

switch(fruit) {
    case 'banana':
        console.log('It is a banana');
        break;  // Exit the switch after the case is executed
    case 'apple':
        console.log('It is an apple');
        break;  // Exit the switch after the case is executed
    case 'orange':
        console.log('It is an orange');
        break;
    default:
        console.log('Unknown fruit');
}
